	AREA RESET, CODE, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	AREA myCode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=UP
	LDR R0,[R0]
	LDR R1,=PACK
	MOV R2,0XF
	MOV R4,#0
	MOV R5,#0
	
DIG	AND R3,R0,R2
	LSL R3,R5
	ADD R5,#4
	ORR R4,R3
	LSR R0,#8
	CMP R0,#0
	BHI DIG
	
	STR R4,[R1]
STOP 
	B STOP
	
UP DCD 0X01020304
	AREA MYDATA, DATA, READWRITE
PACK DCD 0
	END